package com.gersonberger;


class ScoreEntry {

    private int arcanaMusicId;
    private int difficulty;
    private int status;
    private int ex;
    private int miss;

    ScoreEntry(int arcanaMusicId, int difficulty, int status, int ex_score, int miss_count){
        this.arcanaMusicId = arcanaMusicId;
        this.difficulty = this.difficulty;
        this.status = status;
        this.ex = ex_score;
        this.miss = miss_count;
    }

}
